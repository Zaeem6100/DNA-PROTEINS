public class value {
    private  String Condon;
    private String Amino;

    public value(String condon, String amino) {
        Condon = condon;
        Amino = amino;
    }

    public String getCondon() {
        return Condon;
    }

    public void setCondon(String condon) {
        Condon = condon;
    }

    public String getAmino() {
        return Amino;
    }

    public void setAmino(String amino) {
        Amino = amino;
    }
}
